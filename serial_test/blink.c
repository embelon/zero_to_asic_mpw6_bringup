void delay(const int d)
{

    /* Configure timer for a single-shot countdown */
	reg_timer0_config = 0;
	reg_timer0_data = d;
    reg_timer0_config = 1;

    // Loop, waiting for value to reach zero
   reg_timer0_update = 1;  // latch current value
   while (reg_timer0_value > 0) {
           reg_timer0_update = 1;
   }

}

void blink()
{
        reg_gpio_out = 1; // OFF
        reg_mprj_datal = 0x00000000;
        reg_mprj_datah = 0x00000000;

		delay(8000000);

        reg_gpio_out = 0;  // ON
        reg_mprj_datah = 0x0000003f;
        reg_mprj_datal = 0xffffffff;

		delay(8000000);
}

int wait_for_char()
{
    int uart_temp;
    while (uart_rxempty_read() == 1);
    uart_temp = reg_uart_data;
   
    uart_pop_char();
    return uart_temp;
}

void send_uart(int data){
    while (reg_uart_txfull == 1);
    reg_uart_data = data;
}

void main()
{
	int i, j, k;

    reg_gpio_mode1 = 1;
    reg_gpio_mode0 = 0;
    reg_gpio_ien = 1;
    reg_gpio_oe = 1;

    configure_io();

    uart_RX_enable(1);

    /*
    see https://github.com/efabless/caravel_SI_testing/blob/chipignite-dev3-C-tests/caravel_board/firmware_vex/mpw8_tests/uart_recieve_back/uart_recieve_back.c
    and https://github.com/efabless/caravel_SI_testing/blob/chipignite-dev3-C-tests/caravel_board/firmware_vex/mpw8_tests/uart_reception/uart_reception.c
    for examples of serial tests
    */

    int count = 0;
	while (1) {

        count ++;
        char c = wait_for_char();
        switch(c) {
            case 'a':
                print("Hello World !!\n");
                break;
            case 'b':
                print_hex(count, 4);
                print("\n");
                break;
            case 'c':
                print("blinking...\n");
                blink();
                break;
            default:
                print("a: hello\nb: count\nc: blink\n");
                break;
        }
    }
}

